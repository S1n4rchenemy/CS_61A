def merge(s1, s2):
    """ Merges two sorted lists """
    if len(s1) == 0:
        return s2
    elif len(s2) == 0:
        return s1
    elif s1[0] < s2[0]:
        return [s1[0]] + merge(s1[1:], s2)
    else:
        return [s2[0]] + merge(s1, s2[1:])

def mergesort(seq):
    """Merge sort a list
    >>> mergesort([2, 1, 5, 4, 6])
    [1, 2, 4, 5, 6]
    >>> mergesort([4, 3, 2, 1])
    [1, 2, 3, 4]
    """
    if len(seq) == 1:
        return seq
    else:
        a = mergesort(seq[: len(seq) // 2 ])
        b = mergesort(seq[len(seq) // 2 :])
        return merge(a, b)


