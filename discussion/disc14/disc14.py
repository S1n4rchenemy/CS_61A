def widest_level(t):
    """
    >>> sum([[1], [2]], [])
    [1, 2]
    >>> t = Tree(3, [Tree(1, [Tree(1), Tree(5)]), Tree(4, [Tree(9, [Tree(2)])])])
    >>> widest_level(t)
    [1, 5, 9]
    """
    levels = []
    x = [t]
    while False in [t.is_leaf() for t in x]:
        levels += [[t.label for t in x]]
        x = sum([t.branches for t in x], [])
    return max(levels, key=len)



